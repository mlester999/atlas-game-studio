class Canvas {
  constructor(width, height, fill = rgba.clear) {
    this.width = width;
    this.height = height;
    this.data = new Uint8Array(width * height * 4);
    if (fill[3] > 0) this.rect(0, 0, width, height, fill);
  }

  pixel(x, y, color) {
    const px = Math.round(x);
    const py = Math.round(y);
    if (px < 0 || py < 0 || px >= this.width || py >= this.height || color[3] <= 0) return;
    const index = (py * this.width + px) * 4;
    const sourceAlpha = color[3] / 255;
    const targetAlpha = this.data[index + 3] / 255;
    const outAlpha = sourceAlpha + targetAlpha * (1 - sourceAlpha);
    if (outAlpha <= 0) return;
    this.data[index] = Math.round((color[0] * sourceAlpha + this.data[index] * targetAlpha * (1 - sourceAlpha)) / outAlpha);
    this.data[index + 1] = Math.round((color[1] * sourceAlpha + this.data[index + 1] * targetAlpha * (1 - sourceAlpha)) / outAlpha);
    this.data[index + 2] = Math.round((color[2] * sourceAlpha + this.data[index + 2] * targetAlpha * (1 - sourceAlpha)) / outAlpha);
    this.data[index + 3] = Math.round(outAlpha * 255);
  }

  rect(x, y, w, h, color) {
    const x0 = Math.round(x);
    const y0 = Math.round(y);
    const x1 = Math.round(x + w);
    const y1 = Math.round(y + h);
    for (let yy = y0; yy < y1; yy += 1) {
      for (let xx = x0; xx < x1; xx += 1) this.pixel(xx, yy, color);
    }
  }

  strokeRect(x, y, w, h, color, size = 1) {
    this.rect(x, y, w, size, color);
    this.rect(x, y + h - size, w, size, color);
    this.rect(x, y, size, h, color);
    this.rect(x + w - size, y, size, h, color);
  }

  circle(cx, cy, r, color) {
    for (let y = Math.round(cy - r); y <= Math.round(cy + r); y += 1) {
      for (let x = Math.round(cx - r); x <= Math.round(cx + r); x += 1) {
        if ((x - cx) ** 2 + (y - cy) ** 2 <= r ** 2) this.pixel(x, y, color);
      }
    }
  }

  ellipse(cx, cy, rx, ry, color) {
    for (let y = Math.round(cy - ry); y <= Math.round(cy + ry); y += 1) {
      for (let x = Math.round(cx - rx); x <= Math.round(cx + rx); x += 1) {
        if (((x - cx) ** 2) / (rx ** 2) + ((y - cy) ** 2) / (ry ** 2) <= 1) this.pixel(x, y, color);
      }
    }
  }

  strokeEllipse(cx, cy, rx, ry, color, width = 2) {
    for (let t = 0; t < Math.PI * 2; t += 0.006) {
      const x = cx + Math.cos(t) * rx;
      const y = cy + Math.sin(t) * ry;
      this.rect(x - width / 2, y - width / 2, width, width, color);
    }
  }

  diamond(cx, cy, rx, ry, color) {
    for (let y = Math.round(cy - ry); y <= Math.round(cy + ry); y += 1) {
      const row = 1 - Math.abs(y - cy) / ry;
      const w = rx * row;
      this.rect(cx - w, y, w * 2 + 1, 1, color);
    }
  }

  line(x0, y0, x1, y1, color, width = 1) {
    const steps = Math.max(Math.abs(x1 - x0), Math.abs(y1 - y0));
    for (let i = 0; i <= steps; i += 1) {
      const t = steps === 0 ? 0 : i / steps;
      const x = x0 + (x1 - x0) * t;
      const y = y0 + (y1 - y0) * t;
      this.rect(x - width / 2, y - width / 2, width, width, color);
    }
  }
}

function writePng(path, canvas) {
  writeFileSync(path, encodePng(canvas.width, canvas.height, canvas.data));
}

function encodePng(width, height, rgbaData) {
  const raw = Buffer.alloc((width * 4 + 1) * height);
  for (let y = 0; y < height; y += 1) {
    const rowStart = y * (width * 4 + 1);
    raw[rowStart] = 0;
    Buffer.from(rgbaData.buffer, y * width * 4, width * 4).copy(raw, rowStart + 1);
  }
  const signature = Buffer.from([137, 80, 78, 71, 13, 10, 26, 10]);
  return Buffer.concat([
    signature,
    pngChunk("IHDR", Buffer.concat([uint32(width), uint32(height), Buffer.from([8, 6, 0, 0, 0])])),
    pngChunk("IDAT", zlib.deflateSync(raw, { level: 9 })),
    pngChunk("IEND", Buffer.alloc(0))
  ]);
}

function pngChunk(type, data) {
  const typeBuffer = Buffer.from(type, "ascii");
  const crcBuffer = Buffer.concat([typeBuffer, data]);
  return Buffer.concat([uint32(data.length), typeBuffer, data, uint32(crc32(crcBuffer))]);
}

function uint32(value) {
  const buffer = Buffer.alloc(4);
  buffer.writeUInt32BE(value >>> 0);
  return buffer;
}

function crc32(buffer) {
  let crc = 0xffffffff;
  for (const byte of buffer) {
    crc ^= byte;
    for (let k = 0; k < 8; k += 1) crc = crc & 1 ? 0xedb88320 ^ (crc >>> 1) : crc >>> 1;
  }
  return (crc ^ 0xffffffff) >>> 0;
}

function mulberry32(seed) {
  return function next() {
    let t = (seed += 0x6d2b79f5);
    t = Math.imul(t ^ (t >>> 15), t | 1);
    t ^= t + Math.imul(t ^ (t >>> 7), t | 61);
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}

