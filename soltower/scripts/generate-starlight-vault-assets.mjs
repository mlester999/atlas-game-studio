function main() {
  for (const dir of ["banners", "rewards/weapons", "rewards/armor", "rewards/relics", "rewards/costumes", "rarity", "icons", "source"]) {
    mkdirSync(join(outRoot, dir), { recursive: true });
  }

  for (const [file, theme] of banners) {
    writePng(join(outRoot, "banners", file), drawBanner(theme));
  }
  for (const [file, rarity, shape] of rewards) {
    writePng(join(outRoot, "rewards", file), drawRewardIcon(rarity, shape));
  }
  for (const [rarity, style] of Object.entries(rarityStyles)) {
    writePng(join(outRoot, "rarity", `${style.key}-frame.png`), drawRarityFrame(rarity));
  }
  writePng(join(outRoot, "icons/earned-gold.png"), drawCurrencyIcon("earned"));
  writePng(join(outRoot, "icons/locked-gold.png"), drawCurrencyIcon("locked"));
  writePng(join(outRoot, "icons/pity.png"), drawVaultGlyph("pity"));
  writePng(join(outRoot, "icons/rate-up.png"), drawVaultGlyph("rate-up"));
  writePng(join(outRoot, "icons/featured.png"), drawVaultGlyph("featured"));

  writeFileSync(
    join(outRoot, "source/README.md"),
    [
      "# SolTower Starlight Vault Assets",
      "",
      "These PNGs are original deterministic local SolTower Starlight Vault assets generated by `scripts/generate-starlight-vault-assets.mjs`.",
      "",
      "- Banner art: 960x320 RGBA PNGs with text-free fantasy summon compositions.",
      "- Reward icons: 160x160 RGBA PNGs grouped by weapons, armor, relics, and costumes.",
      "- Rarity frames: 160x160 transparent RGBA PNGs for Common, Uncommon, Rare, Epic, Legendary, and Mythical tiers.",
      "- Utility icons: 64x64 transparent RGBA PNGs for gold, pity, rate-up, and featured states.",
      "",
      "These files are committed project assets. Do not replace them with external URLs, emoji, CSS-only shapes, or missing manifest entries."
    ].join("\n")
  );
}

