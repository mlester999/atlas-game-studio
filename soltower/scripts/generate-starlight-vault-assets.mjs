function gradientBackdrop(c, theme) {
  const palettes = {
    featured: [[9, 15, 32, 255], [29, 57, 86, 255], [56, 30, 102, 255]],
    weapons: [[9, 18, 28, 255], [31, 58, 84, 255], [83, 51, 27, 255]],
    armor: [[13, 19, 31, 255], [52, 57, 68, 255], [27, 60, 87, 255]],
    relics: [[11, 17, 34, 255], [57, 36, 104, 255], [21, 79, 96, 255]],
    costumes: [[12, 18, 34, 255], [35, 69, 91, 255], [73, 46, 105, 255]]
  };
  const [top, mid, glow] = palettes[theme];
  for (let y = 0; y < c.height; y += 4) {
    const t = y / c.height;
    const col = lerpColor(top, mid, t);
    c.rect(0, y, c.width, 4, col);
  }
  for (let i = 0; i < 18; i += 1) {
    const x = 60 + i * 52;
    const h = 28 + (i % 4) * 16;
    c.rect(x, 236 - h, 36, h, [glow[0], glow[1], glow[2], 80]);
    c.rect(x + 4, 240 - h, 28, 4, rgba.gold0);
  }
  c.ellipse(480, 168, 280, 90, [glow[0], glow[1], glow[2], 70]);
  c.ellipse(480, 214, 385, 74, [0, 0, 0, 70]);
}

